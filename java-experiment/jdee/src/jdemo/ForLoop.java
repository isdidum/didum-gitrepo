package jdemo;

public class ForLoop {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		/*
		 * a for-loop, on the other hand, combines Initialization Counter, Condition Test, and Update Counter 
		 * into a single expression
		 */
		for(int i=0; i<6; i++){
			System.out.println("i: "+i);
		}
	}

}
